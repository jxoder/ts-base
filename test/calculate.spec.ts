describe('calculate', () => {
  it('should return 2 when adding 1 and 1', () => {
    expect(1 + 1).toBe(2)
  })

  it('should return 2 when subtracting 1 from 3', () => {
    expect(3 - 1).toBe(2)
  })

  it('should return 2 when multiplying 1 and 2', () => {
    expect(1 * 2).toBe(2)
  })

  it('should return 2 when dividing 4 by 2', () => {
    expect(4 / 2).toBe(2)
  })
})
